public class Decimals {
    public static void main(String[] args) {
//        double koristimo za decimalne brojeve
        double percentage = 55.7;
        System.out.println("In 2020, " + percentage + "% of the world's pop is urban. " );

//        double koristimo za matematicke operacije, izbegavati int i long
        double divided = 20;
        double divisor = 6;
        System.out.println(divided/divisor);
    }
}
